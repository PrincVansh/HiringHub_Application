    private void btnApplyForJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyForJobActionPerformed

        int row=tblAllJobs.getSelectedRow();
        if(row==-1)
        {
            JOptionPane.showMessageDialog(null,"Please select a Job First","alert !",JOptionPane.ERROR_MESSAGE);
            return ;
        }
        ResultsPojo obj=new ResultsPojo();
        obj.setpId(CurrentUser.getId());
        obj.setjId((String)model.getValueAt(row,0));
        obj.setPercentage(-1);
        obj.setSelectedByHr("No");
        try{
            boolean result=ResultsDAO.applyForJob(obj);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Applied for job successfully \n Now you can appear for Test","Success",JOptionPane.INFORMATION_MESSAGE);
                new StudentOptionFrame().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"Your could not appear in the test \nBecause You Alredary Applied for this job","Warning",JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Db facing some issues","DB Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnApplyForJobActionPerformed

