    private void loadJobs()
    {
        try{
            List<ParticipantJobPojo> jobs=JobDAO.getAllOpenJobs();
            if(jobs.size()==0)
            {
                JOptionPane.showMessageDialog(null,"No open jobs available","Jobs ?",JOptionPane.INFORMATION_MESSAGE);
                return ;
            }
           
             for(ParticipantJobPojo obj:jobs)
             {
                 Vector<String>v=new Vector<>();
                 v.add(obj.getJobId());
                 v.add(obj.getJobTitle());
                 v.add(obj.getCompanyName());
                 v.add(obj.getTags());
                 
                 model.addRow(v);
             }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB","DB Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

